// Функции

// Функции - это участки кода которые можно переиспользовать, в этом уроке мы не будем
// их рассматривать подробно.

// Для создания функции существует несколько способов ключевое слово function и стрелочный
// синтаксис


function foo(a, b) {
    return a + b;
}

const bar = (a, b) => {
    return a - b;
}

console.log(foo);
console.log(bar);


// Функции также являются объектами и относятся к типу данных object.

